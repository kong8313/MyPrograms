using System;
using System.Linq;
using Confirmit.CATI.Common.ServiceLocation;
using Confirmit.CATI.Core.DAL.Generated.Entity.Table;
using Confirmit.CATI.Core.Repositories;
using Confirmit.CATI.Core.Repositories.Interfaces;
using Confirmit.CATI.Core.Services;
using Confirmit.CATI.IntegrationTests.Framework.Tools;
using Confirmit.CATI.Supervisor.Core.Persons;

namespace Confirmit.CATI.IntegrationTests.Framework.Data.Builders
{
    class PersonGroupDataBuilder : BaseObjectBuilder<PersonGroupData>
    {
        public PersonGroupDataBuilder(TestDataContext context, PersonGroupData data, DataGenerator dataGenerator)
            : base(context, data, dataGenerator)
        {
        }

        public override void Create()
        {
            CheckAndInitData();

            var bvPersonGroupEntity = new BvPersonGroupEntity
            {
                Name = Data.Name,
                Description = Data.Description,
                InboundBehavior = Data.InboundBehavior,
                TransferBehavior = Data.TransferBehavior
            };

            var parentGroups = new []{ PersonManager.GetCatiRootID()};
            int id = PersonManager.CreatePersonGroup(bvPersonGroupEntity, parentGroups);

            Context.PersonGroups.Add(new PersonGroupController(Data.Tag, id, Context, Data));
        }

        private void CheckAndInitData()
        {
            if (Data.Name == null)
            {
                Data.Name = DataGenerator.NewName(Data.Tag ?? "AutoGeneratedGroupName");
            }
        }

        public override void Setup()
        {
             if (Data.Memberships != null)
             {
                 var thisGroupId = PersonGroupRepository.TryGetByName(Data.Name).SID;
                 var groupTags = Data.Memberships.Split(new[] { ',' }, StringSplitOptions.RemoveEmptyEntries);

                 PersonGroupService.SetParentGroups(thisGroupId, Context.GetResources(groupTags).Select(x => x.Id).ToArray());
             }
        }
    }
}
