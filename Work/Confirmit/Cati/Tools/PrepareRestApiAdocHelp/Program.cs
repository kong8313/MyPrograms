using System;
using System.Diagnostics;
using System.IO;
using System.Net;
using System.Windows.Forms;
using Confirmit.CATI.Installation.Common;
using Confirmit.CATI.Installation.Common.Interfaces;
using Confirmit.CATI.WindowsServiceTools;
